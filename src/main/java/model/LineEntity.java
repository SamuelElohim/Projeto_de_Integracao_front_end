package model;



public class LineEntity implements EntityInterface {

    private String name;
    public LineEntity() {}

    public LineEntity(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
